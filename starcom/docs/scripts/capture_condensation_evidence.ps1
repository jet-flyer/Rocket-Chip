    $lines = Get-Content $f -TotalCount 200
    $head = $lines | Out-String
    $contentAll += "----- BEGIN $f (head-200 or full) -----`n$head----- END $f -----`n"
  } else {
    $contentAll += "MISSING: $f`n"
  }
}
Write-Clean "$scratch/all_docs_content.txt" $contentAll

