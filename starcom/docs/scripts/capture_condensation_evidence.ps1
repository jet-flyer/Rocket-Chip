# capture_condensation_evidence.ps1
# The single source of truth for generating clean evidence files in {SCRATCH}.
# Run after any change to DESIGN.md or related.
# Produces only the required files for the verification plan.
# Uses UTF8 no-BOM. Exits 1 on any mojibake detection.
# Also validates the DATA_PRESERVATION_MANIFEST.

$ErrorActionPreference = 'Stop'

$utf8NoBom = New-Object System.Text.UTF8Encoding $false

$repoRoot = Split-Path -Parent (Split-Path -Parent $PSScriptRoot)  # starcom/docs/scripts -> repo root approx; adjust if needed
Set-Location $repoRoot

$scratch = 'C:\Users\pow-w\AppData\Local\Temp\grok-goal-0c77f1f8fcc0\implementer'
New-Item -ItemType Directory -Force -Path $scratch | Out-Null

function Write-Clean {
  param([string]$path, [string]$content)
  [System.IO.File]::WriteAllText($path, $content, $utf8NoBom)
  $readBack = [System.IO.File]::ReadAllText($path)
  # Detect common mojibake / bad chars from previous captures
  if ($readBack -match '[\u00A0�]|A�|â€|Starcom �|\?{2,}' -or $readBack -match '60A�') {
    Write-Error "Mojibake or bad encoding detected in $path"
    exit 1
  }
  Write-Host "Wrote clean: $path"
}

# 1. git_tree_evidence.txt (verif #1)
$branch = git branch --show-current
$statusStarcom = git status --porcelain -- 'starcom/' | Out-String
$logStarcom = git log --oneline -5 --all -- 'starcom/docs/' 'starcom/*.md' | Out-String
$diffNames = git diff --name-only main...HEAD -- 'starcom/' | Out-String
$gitEvidence = @"
=== GIT_TREE_EVIDENCE (generated by capture script) ===
Branch: $branch
git status --porcelain -- 'starcom/':
$statusStarcom
git log --oneline -5 for starcom paths:
$logStarcom
git diff --name-only main...HEAD -- 'starcom/':
$diffNames
Note: Only starcom/ paths are in scope for condensation. Other untracked/dirty (agent-tools/, mcps/, L2P5*) are pre-existing and excluded via filters. No historical documents deleted.
"@
Write-Clean "$scratch/git_tree_evidence.txt" $gitEvidence

# 2. all_docs_list.txt (verif #2)
$list = Get-ChildItem -Recurse starcom -Include *.md | Sort-Object FullName | Select-Object -ExpandProperty FullName | Out-String
$lsOut = (Get-ChildItem starcom, starcom/docs, starcom/docs/research | Out-String)
$allList = "=== ALL STARCOM *.md (sorted) ===`n$list`n=== ls tree ===`n$lsOut"
Write-Clean "$scratch/all_docs_list.txt" $allList

